// 统一订单详情取数：优先 curated ORDER_DATA，否则由所属线索确定性生成
function getOrderDetail(orderId) {
  const curated = ORDER_DATA[orderId];
  const ref = ORDER_INDEX[orderId];
  const clue = ref ? CLUE_DATA[ref.clueId] : null;
  const product = (curated && curated.product) || (clue && clue.product) || ORDER_COMMON.product;
  const broken = !!(clue && (clue.rootTag === '断供型' || /ZZ医药/.test(clue.diagnosis || '')));
  const ctx = {
    clueId:   ref ? ref.clueId : 'xiehe',
    hospital: (curated && curated.hospital) || (clue && clue.hospital) || ORDER_COMMON.hospital,
    product:  product,
    dept:     (curated && curated.dept) || deptOfProduct(product),
    distributor: (curated && curated.distributor) ||
                 (broken ? 'ZZ医药 ' + ((clue && clue.region) || '') : '国药控股 ' + ((clue && clue.region) || '区域仓')),
    payer:    (curated && curated.payer) || ORDER_COMMON.payer,
  };
  if (curated) return Object.assign({ id: orderId }, ctx, curated);

  // ── 确定性生成（无 curated 记录时） ──
  const m = /^ORD-(\d{4})-(\d{2})-(\d{4})$/.exec(orderId);
  const year = m ? m[1] : '2026';
  const mon  = m ? m[2] : '04';
  const seed = hashStr(orderId);
  const day  = String((seed % 26) + 2).padStart(2, '0'); // 02~27
  const date = year + '-' + mon + '-' + day;
  const total = ref ? ref.total : 1;
  const i = ref ? ref.i : 0;
  const frac = total > 1 ? i / (total - 1) : 1; // 0=最新 1=最早（越早量越大，贴合下行叙事）
  const base = 700 + (seed % 700);              // 700~1399
  const qty  = Math.round(base * (0.45 + 0.55 * frac) / 10) * 10;
  const skus = LINE_SKU[lineLetter(product)] || LINE_SKU.A;
  const q0 = Math.round(qty * 0.58 / 5) * 5;
  const q1 = qty - q0;
  const amt0 = q0 * skus[0][2];
  const amt1 = q1 * skus[1][2];
  const items = [
    [skus[0][0], skus[0][1], q0, skus[0][2], (amt0/10000).toFixed(2)],
    [skus[1][0], skus[1][1], q1, skus[1][2], (amt1/10000).toFixed(2)],
  ];
  const rate  = broken ? +(0.58 + (seed % 18)/100).toFixed(2) : +(0.93 + (seed % 7)/100).toFixed(2);
  const delay = broken ? (seed % 6) + 4 : (seed % 3) + 1;
  return Object.assign({ id: orderId }, ctx, {
    date: date, qty: qty, amount: '¥' + ((amt0 + amt1)/10000).toFixed(1) + '万',
    rate: rate, delay: delay, items: items,
  });
}

