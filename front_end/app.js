function filterTable() {
  const tbody = document.getElementById('clues-tbody');
  if (!tbody) return;
  const activeLevel = getActiveFilters('.filter-group[data-type="level"]');
  const activeProd  = getActiveFilters('.filter-group[data-type="product"]');
  const activeRoot  = getActiveFilters('.filter-group[data-type="root"]');
  const rows = tbody.querySelectorAll('tr');
  rows.forEach(row => {
    const lvl  = row.dataset.level  || '';
    const prod = row.dataset.product|| '';
    const root = row.dataset.root   || '';
    const ok = (!activeLevel.length || activeLevel.includes(lvl))
            && (!activeProd.length  || activeProd.includes(prod))
            && (!activeRoot.length  || activeRoot.includes(root));
    row.style.display = ok ? '' : 'none';
  });
}

function getActiveFilters(selector) {
  const group = document.querySelector(selector);
  if (!group) return [];
  return Array.from(group.querySelectorAll('.filter-chip.active'))
    .map(c => c.dataset.value || c.textContent.trim())
    .filter(v => v !== '全部');
}

