// ── Status machine advance ────────────────────────────────────
// Disposition states:
//   step 0 = 待处理 (initial)
//   step 1 = 跟进中
//   step 2 + disposition=claimed  → 已处置·声称挽回 → proceeds to 兑现/未兑现
//   step 2 + disposition=lost     → 已处置·确认流失 (terminal)
//   step 2 + disposition=false    → 已处置·误报 (terminal, reason prompt)
document.addEventListener('click', function(e) {
  const btn = e.target.closest('.btn-advance-status');
  if (!btn) return;
  const step = btn.dataset.step;
  const disposition = btn.dataset.disposition || '';

  const nodes = document.querySelectorAll('.status-node');
  const lines = document.querySelectorAll('.status-line');
  let numericStep = parseInt(step) || 0;

  // Handle misreport separately (open inline modal first)
  if (step === '2' && disposition === 'false') {
    const misreportModal = document.getElementById('misreport-modal');
    if (misreportModal) { misreportModal.classList.add('open'); }
    else { showToast('请选择误报原因', 'info'); }
    return;
  }

  nodes.forEach((node, i) => {
    const circle = node.querySelector('.status-circle');
    const label  = node.querySelector('.status-label');
    if (!circle || !label) return;
    if (i < numericStep) {
      circle.classList.remove('active'); circle.classList.add('done');
      label.classList.remove('active'); label.classList.add('done');
      circle.textContent = '✓';
    } else if (i === numericStep) {
      circle.classList.add('active'); circle.classList.remove('done');
      label.classList.add('active'); label.classList.remove('done');
    } else {
      circle.classList.remove('active','done');
      label.classList.remove('active','done');
    }
  });
  lines.forEach((line, i) => {
    line.classList.toggle('done', i < numericStep);
  });

  let toastMsg = '';
  if (step === '1') {
    toastMsg = '线索状态已更新 → 跟进中';
  } else if (step === '2' && disposition === 'claimed') {
    const node2 = nodes[2];
    if (node2) {
      const lbl = node2.querySelector('.status-label');
      if (lbl) lbl.textContent = '已处置·声称挽回';
    }
    const node3 = nodes[3];
    if (node3) {
      const l = node3.querySelector('.status-label');
      if (l) l.textContent = '兑现核验（系统自动）';
    }
    const verifyNote = document.getElementById('verify-pending-note');
    if (verifyNote) verifyNote.style.display = '';
    const dispBtns = document.getElementById('disposition-buttons');
    if (dispBtns) dispBtns.style.display = 'none';
    toastMsg = '已标记「声称挽回」，系统将在7-14天后自动回填兑现结果';
  } else if (step === '2' && disposition === 'lost') {
    const node2 = nodes[2];
    if (node2) {
      const lbl = node2.querySelector('.status-label');
      if (lbl) lbl.textContent = '已处置·确认流失';
    }
    const node3 = nodes[3];
    if (node3) {
      const c = node3.querySelector('.status-circle');
      const l = node3.querySelector('.status-label');
      if (c) { c.textContent = '—'; c.style.opacity = '0.3'; }
      if (l) { l.textContent = '无需核验（终态）'; l.style.opacity = '0.3'; }
    }
    const dispBtns = document.getElementById('disposition-buttons');
    if (dispBtns) dispBtns.style.display = 'none';
    const lostNote = document.getElementById('disposition-result');
    if (lostNote) { lostNote.textContent = '已确认流失，线索归档。'; lostNote.style.display = ''; }
    toastMsg = '已标记「确认流失」，线索归档，不进入兑现核验';
  } else {
    const statusLabels = ['待处理','跟进中','已处置'];
    toastMsg = '线索状态已更新 → ' + (statusLabels[numericStep] || '处理中');
  }
  showToast(toastMsg, 'success', 3400);
});

