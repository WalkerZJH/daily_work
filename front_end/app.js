// ── Count-up number animation (.count-num) ────────────────────
function animateCount(el, target, duration) {
  const start = 0;
  const startTime = performance.now();
  const fmt = (n) => Math.round(n).toLocaleString('en-US');
  function tick(now) {
    const p = Math.min((now - startTime) / duration, 1);
    const eased = 1 - Math.pow(1 - p, 3); // easeOutCubic
    el.textContent = fmt(start + (target - start) * eased);
    if (p < 1) requestAnimationFrame(tick);
    else el.textContent = fmt(target);
  }
  requestAnimationFrame(tick);
}

document.addEventListener('DOMContentLoaded', function() {
  // Count-up for hero / kpi numbers
  document.querySelectorAll('.count-num').forEach(el => {
    const target = parseFloat(el.dataset.target);
    if (isNaN(target)) return;
    const duration = parseInt(el.dataset.duration) || 1100;
    animateCount(el, target, duration);
  });

  // Stagger entrance for clue cards
  document.querySelectorAll('.clue-list .clue-card.fade-up').forEach((card, i) => {
    card.style.animationDelay = (i * 0.07).toFixed(2) + 's';
  });
});
